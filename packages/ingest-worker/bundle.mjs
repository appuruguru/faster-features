#!/usr/bin/env node
/**
 * Inlines the widget sources into assets.js so the Worker can serve them at
 * /widget.js and /roadmap.js. Run `npm run bundle` after editing the widgets.
 * The output (assets.js) is committed so deploys (button or CLI) need no build.
 */
import { readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const widgetDir = join(here, "..", "widget");

// Standalone worker copies (e.g. from `degit packages/ingest-worker`) don't ship
// the widget sources — that's fine, assets.js is committed. Skip gracefully.
if (!existsSync(join(widgetDir, "widget.js"))) {
  console.log("Widget sources not present (standalone copy) — using committed assets.js.");
  process.exit(0);
}

const widget = await readFile(join(widgetDir, "widget.js"), "utf8");
const roadmap = await readFile(join(widgetDir, "roadmap.js"), "utf8");

// JSON.stringify produces a safe JS string literal regardless of contents.
const out =
  "// AUTO-GENERATED by bundle.mjs — do not edit. Run `npm run bundle` to refresh.\n" +
  `export const WIDGET_JS = ${JSON.stringify(widget)};\n` +
  `export const ROADMAP_JS = ${JSON.stringify(roadmap)};\n`;

await writeFile(join(here, "assets.js"), out);
console.log("Wrote assets.js (widget.js + roadmap.js)");
